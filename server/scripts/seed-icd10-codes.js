async function seedICD10Codes() {
  const client = await pool.connect();

  try {
    await client.query('BEGIN');

    // Check if table exists
    const tableCheck = await client.query(`
      SELECT EXISTS (
        SELECT FROM information_schema.tables 
        WHERE table_schema = 'public' 
        AND table_name = 'icd10_codes'
      );
    `);

    if (!tableCheck.rows[0].exists) {
      console.log('Creating icd10_codes table...');
      await client.query(`
        CREATE TABLE IF NOT EXISTS icd10_codes (
          id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
          code VARCHAR(20) UNIQUE NOT NULL,
          description TEXT NOT NULL,
          code_type VARCHAR(10) DEFAULT 'ICD10',
          parent_code VARCHAR(20),
          chapter VARCHAR(10),
          block VARCHAR(20),
          category VARCHAR(20),
          billable BOOLEAN DEFAULT true,
          valid_for_submission BOOLEAN DEFAULT true,
          effective_date DATE,
          end_date DATE,
          search_vector tsvector GENERATED ALWAYS AS (
            to_tsvector('english', 
              COALESCE(code, '') || ' ' || 
              COALESCE(description, '')
            )
          ) STORED,
          created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
        )
      `);

      await client.query(`
        CREATE INDEX IF NOT EXISTS idx_icd10_search 
        ON icd10_codes USING GIN(search_vector)
      `);

      await client.query(`
        CREATE INDEX IF NOT EXISTS idx_icd10_code 
        ON icd10_codes(code)
      `);
    }

    // Combine cardiology and general codes, then remove duplicates by code
    const allCodesRaw = [...cardiologyCodesRaw, ...generalCodesRaw];
    const uniqueCodes = [];
    const seenCodes = new Set();
    for (const codeData of allCodesRaw) {
      if (!seenCodes.has(codeData.code)) {
        seenCodes.add(codeData.code);
        uniqueCodes.push(codeData);
      }
    }

    console.log(`Seeding ${uniqueCodes.length} unique ICD-10 codes (${cardiologyCodesRaw.length} cardiology + ${generalCodesRaw.length} general, removed ${allCodesRaw.length - uniqueCodes.length} duplicates)...`);

    let inserted = 0;
    let updated = 0;
    let skipped = 0;

    for (const codeData of uniqueCodes) {
      try {
        const result = await client.query(`
          INSERT INTO icd10_codes (code, description, billable, valid_for_submission)
          VALUES ($1, $2, $3, $4)
          ON CONFLICT (code) 
          DO UPDATE SET 
            description = EXCLUDED.description
          RETURNING id
        `, [codeData.code, codeData.description, true, true]);

        if (result.rows.length > 0) {
          inserted++;
        }
      } catch (error) {
        console.error(`Error inserting code ${codeData.code}:`, error.message);
        skipped++;
      }
    }

    await client.query('COMMIT');
    console.log(` Successfully seeded ICD-10 codes:`);
    console.log(`   - Inserted: ${inserted}`);
    console.log(`   - Updated: ${updated}`);
    console.log(`   - Skipped: ${skipped}`);
    console.log(`   - Total: ${uniqueCodes.length}`);

  } catch (error) {
    await client.query('ROLLBACK');
    console.error('Error seeding ICD-10 codes:', error);
    throw error;
  } finally {
    client.release();
  }
}

// Run if called directly
if (require.main === module) {
  seedICD10Codes()
    .then(() => {
      console.log(' ICD-10 code seeding completed');
      process.exit(0);
    })
    .catch((error) => {
      console.error(' ICD-10 code seeding failed:', error);
      process.exit(1);
    });
}

